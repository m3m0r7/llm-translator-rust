pub fn map_lang_for_whisper(lang: &str) -> Option<&'static str> {
    match lang.trim().to_lowercase().as_str() {
        "ja" | "jpn" => Some("ja"),
        "en" | "eng" => Some("en"),
        "zh" | "zho" | "zho-hans" | "zho-hant" => Some("zh"),
        "ko" | "kor" => Some("ko"),
        "fr" | "fra" => Some("fr"),
        "es" | "spa" => Some("es"),
        "de" | "deu" => Some("de"),
        "it" | "ita" => Some("it"),
        "pt" | "por" => Some("pt"),
        "ru" | "rus" => Some("ru"),
        "nl" | "nld" => Some("nl"),
        "sv" | "swe" => Some("sv"),
        "no" | "nor" => Some("no"),
        "da" | "dan" => Some("da"),
        "fi" | "fin" => Some("fi"),
        "pl" | "pol" => Some("pl"),
        "cs" | "ces" => Some("cs"),
        "el" | "ell" => Some("el"),
        "tr" | "tur" => Some("tr"),
        "ar" | "ara" => Some("ar"),
        "hi" | "hin" => Some("hi"),
        "id" | "ind" => Some("id"),
        "vi" | "vie" => Some("vi"),
        "th" | "tha" => Some("th"),
        _ => None,
    }
}

